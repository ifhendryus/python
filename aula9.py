# Operadores aritméticos

adicao = 10 + 10
print('adição', adicao)

subtracao = 10 - 5
print('subtração', subtracao)

multiplicacao = 10 * 10
print('multiplicação', multiplicacao)

divisao = 10 / 5
print('divisão', divisao)

divisao_inteira = 10 // 3
print('divisão inteira', divisao_inteira)

exponenciacao = 2 ** 10
print('Exponenciação')

modulo = 25 % 5
print('Módulo', modulo)